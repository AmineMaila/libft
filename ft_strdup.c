#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*cpy;
	size_t	size;
	int		i;

	size = ft_strlen(s1) + 1;
	cpy = (char *) malloc(size * sizeof(char));
	if (cpy == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		cpy[i] = s1[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
